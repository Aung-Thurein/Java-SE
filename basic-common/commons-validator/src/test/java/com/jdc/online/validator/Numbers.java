package com.jdc.online.validator;

import com.jdc.online.validator.annotation.MaxValue;
import com.jdc.online.validator.annotation.MinValue;

import lombok.AllArgsConstructor;
import lombok.Data;

@Data
@AllArgsConstructor
public class Numbers {

	@MinValue(value = 10)
	@MaxValue(value = 100)
	private byte ByteValue;
	
	@MinValue(value = 10)
	@MaxValue(value = 100)
	private short ShortValue;
	
	@MinValue(value = 10)
	@MaxValue(value = 100)
	private int IntValue;
	
	@MinValue(value = 10)
	@MaxValue(value = 100)
	private long LongValue;
	
}
